public class Employee {
    int EmployeeID;
   // name, age, job title, and salary
    String name;
    int age;
    String jobTitle;
    int salary;
    Employee(int EmployeeId, String name, int age, String jobTitle, int salary){
        this.EmployeeID = EmployeeId;
        this.name = name;
        this.age = age;
        this.jobTitle = jobTitle;
        this.salary = salary;
    }
}

